package FinalActivity;

public class SecondActivity {


    // Add
        public static int addition(int x, int y) {
            int sum = x + y;
            return sum;

        }

        // Subtract
        public static int subtract(int x, int y) {
            int diff = x - y;
            return diff;
        }

        // Multiply
        public static int multiply(int x, int y) {
            int multiple = x * y;
            return multiple;

        }

        // Divide
        public static int divide(int x, int y) {
            int divValue = x / y;
            return divValue;

        }


}


